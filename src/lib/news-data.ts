// Get featured article (most recent)
export const getFeaturedArticle = (): NewsArticle => {
  return mockNewsArticles[0];
};

// Get articles by category
export const getArticlesByCategory = (category: NewsCategory | 'All'): NewsArticle[] => {
  if (category === 'All') {
    return mockNewsArticles;
  }
  return mockNewsArticles.filter(article => article.category === category);
};

// Get article by ID
export const getArticleById = (id: string): NewsArticle | undefined => {
  return mockNewsArticles.find(article => article.id === id);
};

// Get trending topics (categories with most articles)
export const getTrendingTopics = (): NewsCategory[] => {
  const categoryCounts = newsCategories.map(category => ({
    category,
    count: mockNewsArticles.filter(a => a.category === category).length,
  }));
  
  return categoryCounts
    .sort((a, b) => b.count - a.count)
    .slice(0, 4)
    .map(item => item.category);
};
